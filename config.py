#   L1（单次对话摘要）系数 = 1.0
#     → 主力检索层，不额外加权，以语义相关度原始排序为准
#
# 调整建议：
#   如果发现模型回复时"话题感知"不足（没有感知到长期规律），可以把 L2 系数调小
#   如果发现模型回复时"细节感知"不足（缺少具体事件细节），可以把 L1 系数调小
#   两个系数的绝对值不重要，重要的是相对大小

