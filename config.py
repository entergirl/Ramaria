# =============================================================================
# 检索层级权重配置
# =============================================================================
#
# 融合 L1 / L2 检索结果注入 prompt 时，对各层的 adjusted_distance 乘以对应系数，
# 得到 final_score，块内按 final_score 升序排列（越小越靠前）。
#
# 系数含义：
#   系数 < 1.0 → 这一层在同等语义距离下更容易排前面（相当于"加分"）
#   系数 = 1.0 → 不额外加权，直接用 adjusted_distance
