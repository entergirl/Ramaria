# =============================================================================
# 记忆衰减配置（Ebbinghaus 遗忘曲线）
# =============================================================================
#
# 衰减公式：R = e^(-t / S)
#   R：记忆保留率，范围 0~1，越大越容易被检索到
#   t：记忆生成距今的天数（based on created_at）
#   S：稳定性系数，越大衰减越慢
#
# 检索分数调整：adjusted_distance = semantic_distance / max(R, 0.1)
#   保留率越低 → adjusted_distance 越大 → 排序越靠后 → 越难被召回
#   max(R, 0.1) 确保极老记忆的分数不会无限爆炸（最低保留率锚定 10%）
#
# 各层 S 值的设计逻辑：
#   L0 原始切片   S=10  → 10天后保留率约 37%，细节信息衰减最快
#   L1 单次摘要   S=30  → 30天后保留率约 37%，衰减适中
#   L2 聚合摘要   S=60  → 60天后保留率约 37%，经过提炼的记忆衰减最慢
#
# 如需调整衰减速度：
#   S 值调大 → 衰减变慢（记忆保留更久）
#   S 值调小 → 衰减变快（旧记忆更快退场）

# 各层稳定性系数（天）
MEMORY_DECAY_S_L0 = 10   # L0 原始消息切片
MEMORY_DECAY_S_L1 = 30   # L1 单次对话摘要
MEMORY_DECAY_S_L2 = 60   # L2 时间段聚合摘要

# ── last_accessed_at 保底加成开关 ──
#
# 背景：某条记忆可能创建于很久以前，但最近被检索命中过（说明它仍然相关）。
# 如果只按 created_at 衰减，这条记忆会因为"年龄大"而持续被压制。
# 保底加成机制：如果一条记忆在 RECENT_BOOST_DAYS 天内被访问过，
# 则其保留率 R 不低于 RECENT_BOOST_FLOOR（默认 0.5），相当于给它"续命"。
#
# 注意：这是保底（floor），不是替换 t。
# 错误理解：用 last_accessed_at 替换 created_at 算 t
#   → 创建于 200 天前但昨天访问的记忆会得到 R≈1，排得极靠前，不合理。
# 正确理解：created_at 始终用于主衰减，last_accessed_at 只在满足条件时
#   给 R 设一个下限，防止"仍在被用的旧记忆"掉得太厉害。
#
# 默认关闭（False）：
#   · 开发阶段无需关心 last_accessed_at 字段是否存在
#   · 数据库迁移（add_last_accessed_at_columns）完成后再开启
MEMORY_DECAY_ENABLE_ACCESS_BOOST = True

# 判定"最近被访问"的时间窗口（天）
# 在此窗口内被命中过的记忆，才触发保底加成
MEMORY_DECAY_RECENT_BOOST_DAYS = 7

# 保底保留率（0~1）
# 最近被访问过的记忆，R 不低于此值
# 0.5 表示即使是很老的记忆，只要最近被用过，其调整后距离最多翻倍（/0.5）
MEMORY_DECAY_RECENT_BOOST_FLOOR = 0.5

# ── salience 对衰减稳定性的加成系数 ──
#
# 衰减公式调整后：
#   decay_s_adjusted = decay_s × (1 + salience × SALIENCE_DECAY_MULTIPLIER)
#
# 含义：
#   salience=0.0 → decay_s 不变，衰减速度正常
#   salience=0.5 → decay_s × 1.25，衰减速度减慢 25%
#   salience=1.0 → decay_s × 1.5，衰减速度减慢 50%（高显著记忆更持久）
#
# 调整建议：
#   系数调大 → 高显著记忆保留更久，但可能导致旧的重要记忆长期占据检索结果
#   系数调小 → 情感权重影响更弱，趋近于纯时间衰减
SALIENCE_DECAY_MULTIPLIER: float = 0.5


